#include "so_long.h"

void	get_player_init_pos(t_game *g, char *line, int y)
{
	int	x;

	x = 0;
	while (line[x])
	{
		if (line[x] == 'P')
		{
			g->px = x;
			g->py = y;
		}
		x++;
	}
}
